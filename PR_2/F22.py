

def f22(x):
    a = x & 0b00000000000000000000000000111111
    b = x & 0b00000000000000001111111111000000
    c = x & 0b00000011111111110000000000000000
    d = x & 0b11111100000000000000000000000000
    b = b >> 6
    c = c >> 16
    d = d >> 26
    numb = (c << 22) | (b << 12) | (d << 6) | a
    return hex(numb)




